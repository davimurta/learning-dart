void main() {
  // Variables

  //String
  var name = "John";
  String name2 = "John";
  print(name);
  print(name2);

  //Integer
  var x = 41;
  int y = 41;
  print(x);
  print(y);

  //dynamic
  dynamic firstName = "tim";
  print(firstName);

  //Const and Final
  const String fullName = "John Elder";
  print(fullName);
  final String nickname = "J";
  print(nickname);
}