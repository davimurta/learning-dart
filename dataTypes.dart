void main() {
  //Data Types 
  // Numbers, Strings, Booleans, Lists, Maps, Dynamic

  //Strings
  String first = "something"; 
  print("String $first");

  // Integers
  int myNum = 41;
  print("integer: $myNum");

  // doubles
  double OtherNum = 41.21;
  print("double: $OtherNum");

  // Booleans
  bool myBool = true;
  print("bool: $myBool");
}